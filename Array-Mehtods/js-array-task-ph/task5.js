// Combining Arrays using the concat Method

// Create two arrays of your choice
const numbers = [23, 12, 2, 6, 15, 17];
const names = ["Mehedi", "Shohan", "Lenovo", "Hasan", "PH", "JavaScript"];

// Use the concat method to combine the two arrays into a new array.
const mixedArray = numbers.concat(names); // Merging two arrays

// Print both the original arrays and the combined array using console.log().
console.log(mixedArray); // Output: [23, 12, 2, 6, 15, 17, "Mehedi", "Shohan", "Lenovo", "Hasan", "PH", "JavaScript"]
console.log(numbers); // Output: [23, 12, 2, 6, 15, 17]
console.log(names); // Output:  ["Mehedi", "Shohan", "Lenovo", "Hasan", "PH", "JavaScript"]
